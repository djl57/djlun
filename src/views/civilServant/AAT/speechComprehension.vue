        <h2 id="ch2-2">语句填空题（2-3题）</h2>
        <p>只要会技巧，就能做出来</p>
        <h3>提问方式单一：填入横线部分最恰当的是</h3>

        <h3>横线在结尾</h3>
        <p>分 --  总 结构。1、总结前文；2、提出对策。（类似中心理解题）</p>
        <p>要保持前后话题一致。</p>

        <h3>横线在开头</h3>
        <p>总 -- 分 结构。需概括文段的中心内容。</p>

        <h3>横线在中间</h3>
        <p>承上启下的作用。注意与上下文联系。把握好主题词，保证文段话题一致。</p>
      </section>
      <section>
        <h2 id="ch2-3">接语选择题（1题）</h2>
        <p>难度不高，送分题。</p>

        <h3>提问方式</h3>
        <p>作者接下来最可能讲述的是（）</p>

        <h3>解题要点</h3>
        <p>重点关注文段最后一句话。</p>
        <p>1、有且仅有一个选项与尾句话题一致，则直接选；</p>
        <p>2、如果有两个及两个以上选项与尾句话题一致，那就需要结合前文分析。</p>
        <p>干扰项特征：文段中已经论述过得内容（容易把这类题当做中心理解题来做，所以中心句一定不是正确答案。）</p>
        <p>文段提出问题，选项中既有原因又有对策，则选原因。</p>

