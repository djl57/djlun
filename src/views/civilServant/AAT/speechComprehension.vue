      <h1 id="ca3">逻辑填空（10-20道）</h1>
      <p>题量最大，难度最高。忌语感。对词汇量要求高。通过刷真题的方式去积累词汇量（宠词（本省））</p>
      <section>
        <h2 id="ch3-1">词的辨析（选项角度）</h2>

        <h3>1. 词义侧重（考的较少）</h3>
        <p>形近音近词：品行、品性</p>
        <div class="bold">方法：</div>
        <p>1、拆字组词：行为、性格</p>
        <p>2、整体搭配：拆字不好拆时：品行端正、品行纯良</p>

        <h3>2. 固定搭配（重要）</h3>
        <p>凝聚共识、考量影响、延误时机、施展才华、活力迸发、大自然的鬼斧神工（通常形容大自然，也可以用来形容建筑、雕塑）
          、人的精雕细刻、人的巧夺天工、人的匠心独运、摄入（具体营养）、汲取（文化营养）、奉为圭臬niè、
          同日而语（同一类对比）、相提并论（有差距的两个事物放在一起谈论）、无法望其项背=望尘莫及、
          旷日持久（强调时间长）、山穷水尽（表示没了）、左支右绌chù（力量不足，应付不过来）、羁绊（缠住不能脱身，束缚）、
          贻笑大方（让内行人笑话）、钳制（不能钳制自己）、分歧（两个以上的观点才有分歧）、门口罗雀（主语一般为地方）、
          疾言厉色（人发火时的面貌）、如临大敌（好像面对这强大的敌人。）、守望相助（面对困难、灾祸时共同面对）、
          无与伦比（好到了极点，过于绝对，一般不是正确选项）、和而不同（和睦地相处,但不随便附和）、执迷不悟（不悔改）、
          一意孤行（行动上，而不是心理上）、独树一帜（独自树立一种风格，与众不同）、相形见绌（两个事物对比显出不足）、
          力所不逮（能力达不到）、经天纬地（形容有治理天下的经世之才）、汪洋恣肆（形容文章、言论书法等气势豪放，潇洒自如）、
          戎马倥偬kǒng zǒng（形容军务繁忙）</p>
        <div class="bold">1、常用词搭配：</div>
        <p>爱护（人[对晚辈]、物）/爱戴（人[对领导]）；（物种、人烟、头发）稀少/（资源）稀缺</p>
        <div class="bold">2、热点词搭配：</div>
        <p>经济新常态、供给侧结构性改革、京津冀协同发展...（和申论一起积累）</p>

        <h3>3. 程度轻重（对比选项确定）</h3>
        <p>所填词语的程度与文段意思的轻重保持一致。比如：缺陷>缺点>瑕疵；截然相反>大相径庭；尴尬程度：羞愧难当>窘态百出</p>

        <h3>4. 感情色彩（对比选项确定）</h3>
