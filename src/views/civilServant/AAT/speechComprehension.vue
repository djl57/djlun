      <section>
        <h2 id="ch2-1">语句排序题（2-3题）</h2>
        <h3>提问方式不用关注</h3>
        <p>出现 1 2 3...的序号排序就是语句排序题。</p>
        <p>解题时要从选项入手。技巧性很强。不要靠语感。</p>
        <h3>解题3步走战略</h3>
        <p>一、看选项，对比后确定首句。</p>
        <p>二、（1）确定捆绑集团【中间不允许有另一句话】（2）确定顺序【中间可以有其他语句，但是顺序不能调换】（3）确定尾句【对比确定】</p>
        <p>三、验证自己锁定的正确答案。</p>
        
        <h3>确定首句</h3>
        <div class="bold">1、背景引入</div>
        <p>关键词：随着、近年来、在...大背景下/环境下</p>
        <p>先看关键词，再对比选项确定首句。</p>
        <div class="bold">2、提出观点</div>
        <p>关键词：有人说、人们普遍认为</p>
        <p>先看关键词，再对比选项确定首句。</p>
        <div class="bold">3、下定义</div>
        <p>关键词：...就是/是指</p>
        <p>先看关键词，再对比选项确定首句，对比首句的时候，先看形式（观点/定义），但还要注意内容。</p>
        <div class="bold">4、非首句特征</div>
        <p>（1）关联词后半部分（2）指代词【人称代词：它/他/她；指示代词：这/那】</p>
        <p>先看关键词，再对比选项确定首句。</p>

        <h3>确定捆绑集团</h3>
        <div class="bold">1、指代词捆绑</div>
        <p>关键词：这、那、他、该、其；每一次、每一类型、每一XX（前有多个内容）</p>
        <div class="bold">2、关联词</div>
        <p>关键词：1、配套出现：不但...而且；（太简单，基本不会这么考）2、单独出现：但、同时</p>
        <div class="bold">3、共同信息捆绑</div>
        <p>关键词：找特殊信息捆绑。</p>

        <h3>确定顺序</h3>
        <div class="bold">1、时间顺序</div>
        <p>年份、朝代、以前...现在...</p>
        <p>不用考虑倒叙、插叙的问题。</p>
        <div class="bold">2、逻辑顺序</div>
        <p>1、行文脉络：先观点+再举例；先提出问题+再提出对策</p>
        <p>2、A和B（并列结构）：应该先说A，再说B</p>

        <h3>确定尾句</h3>
        <p>结论+对策</p>
        <p>关键词：因此、所以、看来、于是、这、应该、需要</p>
      </section>
