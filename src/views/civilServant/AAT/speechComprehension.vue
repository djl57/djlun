      <section>
        <h2 id="ch1-1">中心理解题（10道左右）</h2>
        <h3 class="bold">提问方式</h3>
        <p>这段文字 主要/旨在/重在/意在/想要说明/主旨（已经跟意图没有区别）/主题/观点/概括最佳当。</p>
        <p class="err">注意 与主旨无关 的提问方式。</p>
        <h3 class="bold">解题思路</h3>
        <p>1、重点词语：关联词、主题词、程度词</p>
        <p>2、行文脉络：指的是文段的写作思路或者文章的结构</p>
        <p>先找关联词（转折、因果），再看主题词、程度词，结合行文脉络。</p>

        <h3>关联词</h3>
        <div class="bold" id="it1-1-1">转折关系：</div>
        <p>虽然...但是...；尽管...可是...；...不过...；...然而...；...却...；其实/事实上/实际上...；殊不知...；
          截然不同；截然相反；全新的研究；一种误读；相对而言；
        </p>
        <p>转折之后是重点，结合行文脉络</p>
        <p>以【近年来】开头，说明这句话说明背景，略读，读出文章话题。以【比如、如】开头，说明这句话是在举例子，作用是加强论证。</p>
        <p>选项中，转折之前的内容不能选（非重点）；优选表述明确的选项（对比其他选项得来）</p>
        <p>常见挖坑方式：1、无中生有；2、非重点的表述；3、优先相对温和的表述，过于绝对的一般不选；4、优选表述明确的选项。（对比选项得出明确选项，比如一个选项有利于，一个选项不利于，那优选有利于）</p>
        <p>逆向思维：如果转折之后内容太多，占用太多时间，就可以用逆向思维，将转折前的内容反过来。</p>
        <p>使用题型文段特征：“很多人/大多数人都以为”、“不少人/传统观点认为” + 转折表述。</p>

        <div class="bold" id="it1-1-2">因果关系：</div>
        <p>因为...所以...；由于...因此...；因而...；故而...；于是...；可见...；看来...</p>
        <p>导致/致使/使得/造成...（后面跟一些不好的结果）；</p>
        <p>选项中出现为什么/为何，后面跟的是结果（比如：欧阳为什么迟到。结果是迟到。）</p>
        <p>如果结论词出现在尾句：大部分情况下是对前文的总结，其之后是文段的中心。</p>
        <p>如果结论词出现在开头/中间，之后仍有其他语句。</p>
        <p>1、之后的语句是进一步解释说明，此时中心句仍是结论句。</p>
        <p>2、之后又出现并列、因果、转折，需结合多种关联词共同分析。（因为我爱吃，所以我很胖，但是我嫁出去了（中心）。）</p>
        <p>结论是重点。</p>
        <p>不明确的选项一定不能选。</p>
        
        <div class="bold" id="it1-1-4">必要条件关系：</div>
        <p>只有...（重点）才...</p>
        <p>必要条件是重点。必要条件即是“只有”和“才”之间的部分。</p>
        <p>言语中不会考充分条件（只要...就...），这种表述过于绝对。</p>
        <p>【最新考点】：省略“只有”（比如：“只有好好学习，才能天天向上。”和“好好学习，才能天天向上。”）</p>

        <div class="bold" id="it1-1-5">对策：</div>
        <p>必要条件的变形。“只有好好学习，才能天天向上”->“”</p>
        <p>对策标志词:</p>
        <p>1、应该、应当、必须、需要、亟待、亟待 + 做法 （情态动词引导对策）</p>
        <p>2、通过/采取...手段/途径/措施/方式/方法/渠道，才能...（通过名词引导对策）</p>
        <p>3、呼吁、倡导、提倡、提醒、建议 + 做法 （通过动词引导对策）</p>
        <p>4、前提、基础、保障（通过名词引导对策，出现频率少）“好好学习是天天向上的前提”</p>
        <p>对策行文脉络：</p>
        <p>1、提出问题-分析问题-<span class="err">解决问题</span>（对策，重点）</p>
        <p>2、对策（重点）+正反论证/原因论证</p>
        <p>3、提出问题-<span class="err">解决问题</span>（对策，重点）-意义效果</p>
        <p>【补充技巧】：例子本身不重要，但是如果做题发现中心句似乎很难理解，这时候可以通过例子来帮助理解，从而得到答案。</p>
        <p>当文段中只有“提出问题-分析问题”的部分时，“解决问题”通常会作为正确答案出现在选项里。</p>
        <div class="bold">反面论证思维：</div>
        <p>典型格式：如果/倘若/一旦...（这里反过来就是正确答案） + 不好的结果</p>
        <p>把前面的做法反过来，即为正确答案</p>
        <p>【注意】：既有正面对策又有反面论证，那么答案就是正面对策，没有正面对策的时候再把反面论证反过来。</p>

        <div class="bold" id="it1-1-7">并列关系：</div>
        <p>没有侧重，概括全面完整</p>
        <p>文段特征：</p>
        <p>1、包含并列关联词。如：此外、另外、同时、以及、“；”</p>
        <p>2、层次分明，格式工整。</p>
        <p>3、无明显其他关联词语。</p>
        <p>选项特征：</p>
        <p>1、两方面情况：和、及、与、同</p>
        <p>2、更多种情况：许多、一些、不同、各种、一系列</p>
        <p>当分句表达的意思相同，则提取共性。</p>

        <h3 id="it1-1-3">主题词</h3>
        <p>起一个辅助功能。当觉得题目太难时，就使用这个技巧。</p>
        <div class="bold">主题词特征：</div>
        <p>1、文段围绕其展开</p>
        <p>2、一般高频出现（主题词可能被指代为 其/该/它）</p>
        <p>3、大多为名词</p>
        <p>4、前有引入，后有解释说明</p>
        <p>常见挖坑方式：1、概念扩得太大（即选项不明确）；</p>

        <h3 id="it1-1-6">程度词</h3>
        <p>起一个辅助功能。当觉得题目太难时，就使用这个技巧。</p>
        <div class="bold">标志词：</div>
        <p>更、尤其、正是、特别是、真正、根本、最核心/突出等</p>
        <p>程度词所在的语句通常为重点。</p>
        <p>开头部分大多是引出话题、或某概念。</p>

        <h3 id="it1-1-8">行文脉络</h3>
        <div class="bold">五种行文脉络：</div>
        <p>1、总 -- 分       （对策 + 原因 / 观点 + 解释说明（例如...））</p>
        <p>2、分 -- 总       （结论。对策 / 末尾因此... / 代词“这”、“此”引导的尾句需关注。）</p>
        <p>3、总 -- 分 -- 总</p>
        <p>4、分 -- 总 -- 分 （随着...但是...例如...）</p>
        <p>5、分 -- 分       （并列结构）</p>
        <p>把握<span class="err">中心句</span>及<span class="err">分述句</span>的特点。</p>
        <p>当中心句不太好确认时，再去找分述句。</p>
        <div class="bold">中心句特征：</div>
        <p>观点（对策、结论、评价）</p>
        <div class="bold">分述句特征（作用：加强论证）：</div>
        <p>1、举例子：“比如”、“例如”、“...就是例证”等</p>
        <p>2、调查报告、数据资料等</p>
        <p>3、正反论证 （对策 + 正反论证）</p>
        <p>4、原因解释</p>
        <p>5、并列分述</p>
        <p>分述句中出现的关联词（但是...、因此...）并不代表重点。</p>
      </section>
      <section>
        <h2 id="ch1-2">细节判断题</h2>
        <p>一般较晦涩难懂。通读全文，定位选项。</p>
        <h3>提问方式</h3>
        <p>“以下对文段理解正确/<span class="err">不</span>正确的是...”；“符合/不符合这段话意思的是...”；
        “从文段中可以得知/推出的是...”</p>
        <h3>典型细节题</h3>
        <div class="bold">错误选项类型：</div>
        <p>1、无中生有</p>
        <p>2、偷换概念（替换概念、混搭概念）</p>
        <p>3、偷换时态：将来时（将要、立刻、趋势、以后）；完成时（已、已经、了、完成）；进行时（正在...、在...中、着）</p>
        <p>4、绝对表述</p>
        <p>表述太绝对要注意（“最”）。回答这种题并不是总结全文，而是拎出部分，比较是否正确。</p>
        <p>当选项中的内容都不超过5个字时，看完提问方式后看选项，再读全文。</p>

        <h3 id="it-1-2-1">相对绝对法</h3>
        <p>当提问说法正确时，先看相对标志词的选项，优先验证，快速答题；当提问说法错误时，先看绝对标志词的选项。</p>
        <div class="bold">绝对标志词（大概率是错误选项）：</div>
        <p>一定、全部、完全、所有、均、永远、任何、取代、主导、唯一、只要...就...、无论...都...</p>
        <div class="bold">相对标志词（大概率是正确选项）：</div>
        <p>可能、也许、或许、大概、一般、尽量、往往、容易、...之一、我永远支持共产党。</p>

        <h3 id="it1-2-2">细节主旨化</h3>
        <p>优选契合主旨的选项（先按着中心理解题来找主旨）。此方法不常用。</p>
