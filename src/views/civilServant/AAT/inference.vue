          <p>2. 并列关系</p>
          <p>（1）矛盾关系（要么...要么...(没有第三种可能)）：生：死【也可以是反义关系】；对称：不对称。</p>
          <p>（2）反对关系（可以是...可以是...(还可能是第三种情况)）：红色：白色；苹果：香蕉；扇子：空调【有相同或相似的功能也可以看成并列】。</p>
          
          <p>3. 包容关系</p>
          <p>（1）种属关系：A是B的一种【苹果：水果】</p>
          <p>（2）组成关系：A是B的一个组成部分【轮胎：汽车；针筒是注射器的组成部分】</p>
          
          <p>4. 交叉关系（比较难的考点）</p>
          <p>并交补的交【两者之间有关系，但不全等A==B】</p>
