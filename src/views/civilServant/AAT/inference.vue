        <p>也不相似（3）优先考虑【属性规律】（属性比较好看出来）；</p>
        <p>（4）其次考虑【数量规律】</p>

        <section>
          <h3 id="ch1-1">1. 位置规律</h3>
          <div class="bold">特征：</div>
          <p>元素组成相同（比如都由小黑三角形和小白三角形组成）</p>
          <div class="bold">考点：</div>
          <p>1、平移：</p>
          <p>（1）方向：直线【上下、左右、斜对角线】；绕圈【顺逆时针】</p>
          <p>（2）方向确定后，看步数：恒定；递增【等差】</p>
          <p>2、旋转、翻转：</p>
          <p>（1）旋转：方向【顺、逆时针】；方向确定后，看常见角度【45、90、180】【肉眼可辩】</p>
          <p>（2）翻转：左右翻转一样【图形沿竖轴对称】；上下翻转一样【图形沿横轴对称】</p>
        </section>

        <section>
          <h3 id="ch1-2">2. 样式规律</h3>
          <div class="bold">特征：</div>
          <p>元素组成相似：（1）元素重复出现；（2）线条重复出现</p>
          <div class="bold">考点:</div>
          <p>1、遍历（九宫格和两组图中居多）：缺啥补啥。</p>
          <p>（1）看外形；</p>
          <p>（2）看填补填充图案。</p>
          
          <p>2、加减同异（可能和位置规律符合考察）：</p>
          <p>（1）相加、相减：图1的线条+图2的线条=图3的线条；</p>
          <p>（2）求异（去同求异）：图1的线条+图2的线条-图1图2一样的地方=图3的线条；</p>
          <p>（3）求同（去异求同）：图1的线条+图2的线条-图1图2不一样的地方=图3的线条。</p>
          
          <p>3、黑白运算：</p>
          <p>（1）特征：图形轮廓和分割区域都相同，不同区域“黑白”颜色不同，且黑块数量不成规律【如果数量一样，优先考虑位置平移规律】；</p>
          <p>（2）方法：相同位置运算【都找左上角，都找右上角...】。</p>
        </section>

        <section>
          <h3 id="ch1-3">3. 属性规律</h3>
          <div class="bold">特征：</div>
          <p>元素组成不相同、不相似：元素没有共性，优先考虑属性。</p>
          <div class="bold">考点:</div>
          <p>1、对称性（考的多，做不出来时考虑）：</p>
          <p>（1）轴对称【五角星、箭头、等边三角形、等腰三角形】：方向【横着对称、竖着对称、斜着对称】；数量【五角星有五条对称轴】；</p>
          <p>（2）中心对称：绕某个点旋转180度重合，不确定就转卷子【N Z S 平行四边形】。</p>
          
          <p>2、曲直性：</p>
          <p>（1）全曲线【月亮、笑脸】；</p>
          <p>（2）全直线；</p>
          <p>（3）曲 + 直（考的少）。</p>
          
          <p>3、开闭性【一个好好的图形开了个口，优先考虑开闭性】：</p>
          <p>（1）全封闭【把一只小仓鼠选放在图形里面，出不来就是全封闭】；</p>
          <p>（2）全开放。</p>
        </section>

        <section>
          <h3 id="ch1-4">4. 数量规律</h3>
          <div class="bold">特征：</div>
          <p>元素组成不相同、不相似，数量规律明显</p>
          <div class="bold">考点:</div>
          <p>点、线、角、面、素【元素】</p>
          <p>1、点数量【线和线的交点、切点算，端点不算】：</p>
          <p>（1）特征：线条交叉明显；乱糟糟的一团线交叉；相切较多。</p>
          <p>（2）【特殊考点：数曲直交点】所有点都数无规律，那就看曲直交点数。</p>

          <p>2、线数量【线条数】：</p>
          <p>特征：“五角星”；“日”、“田”及其变形；圆相切、相交；出现明显端点。</p>
          <p>（1）直线数：多边形、单一直线；</p>
          <p>（2）曲线数【光滑画过来的算一条，明显被折的算分开数】：全曲线图、圆、弧。</p>
          <p>（3）【特殊考点：笔画问题】是否能一笔画成【圆相切、相交一定是一笔画图形】：
          <p>一笔画成只要满足两个条件：（1）线条之间连通；
            （2）奇点数=2或=0（<span class="err">端点一定是奇点</span>）【奇点：发射出奇数条线的点】</p>
          <p>能多笔画成的图形：笔画数=奇点数/2【任何图形的奇点数一定是偶数个】（ <span class="err">注意：端点一定不能忘</span> ）。</p>
          
          <p>3、角数量</p>
          <p>特征：扇形、折线较多、改造图（构图不完整、留小缺口）【先看开闭性，再数角】</p>
          <p>（1）什么是角？直线与直线相交才是角（直线与曲线相交不是角）；小于180度的角；角不能重复数【学的时候都能重复数】</p>

          <p>4、面数量</p>
          <p>特征：图形被分割、封闭面明显；生活化图形、粗线条图形中留白区域【先看开闭性，再数面】。</p>
          <p>（1）什么是面？封闭空间（不封闭是0个面）；面是白色的，不是黑色的（因为封闭空间肯定是用黑线画的）。</p>
          <p>【特殊考点：每个面的形状；最大面的形状。】</p>
          
          <p>5、素数量</p>
          <p>特征：出现多个小图形；生活化图形、粗线条图形【先看对称性、封闭性，有明显面则数面，多个小图形数部分数】。</p>
          <p>（1）元素种类</p>
