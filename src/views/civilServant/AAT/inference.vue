          <p>涉及物理变化的工艺：捆扎、打磨、雕琢、剪裁、织造等。</p>
          <p>涉及化学变化的工艺：燃烧、氧化、发酵、酿造、冶炼等。</p>
          <p>物理、化学区别：有无新物质生成。</p>
          <p>（3）功能：手机：通讯。</p>
          <p>（4）属性/特点：糖：甜。“属性”二级辨析：必然【A一定是B样的】/或然属性</p>
          <p>（5）时间顺序：购票：乘车【先购票，再乘车】。“时间顺序”二级辨析：行为发出的主体。</p>
          <p>（6）必要条件：沸点：沸腾【只有达到沸点，才会沸腾】。</p>
          <p>（7）因果：炎热：中暑。</p>
          
