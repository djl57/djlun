        <p class="err">注意求月均、日均，然后资料中给的是全年的题</p>
        <p>求一堆的的平均值：削峰填谷：找一个数（相对居中好算），算其他的+5，-16，+7...如果加起来为0就是平均数，如果大于0，多的部分求平均值，加到找的那个数上就是要求的平均值。</p>
        <p>求大于小于由多少的题太难算时，可以先列个式子，然后转化为好算的式子：</p> 
        <p>【例如：给“12月”和“全年”的数据，让比较“12月”和“1-11月的平均值”】</p>
        <p>【方法：直接比较“12月”与“全年的平均值” 或者 直接比较“12月×12”与“全年”】</p>
        <p>求高于平均数的数值，结合选项，对数值进行排序，如选项有四个数值，就从高到低选出前四个数值。</p>
        
        <h3 id="ch7-2">基期平均数</h3>
        <p>识别：求上一年的平均数</p>
