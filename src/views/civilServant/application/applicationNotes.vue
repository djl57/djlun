        <p>速算：（1）截位直除（多步除法）（2）先算A/B，再看(1+b)/(1+a)与1的关系</p>
        <p class="err">注意：截位直除，一步除法，只截分母，多步除法才分子分母都截。</p>

        <h3 id="ch7-3">两期平均数</h3>
        <div class="bold">比较：</div>
        <p>识别：两个时期+平均数+上升/下降</p>
        <p>遇两期平均比较，看a（分子增数）和b（分母增数），a>b上升,a&lt;b下降，a=b一样</p>
        <div class="bold">计算：</div>
        <p>两个时期+平均+上升/下降+百分之几</p>
        <p>公式：平均数的增长率 = (a-b)/(1+b)</p>
        <p>不能选最小，只能套公式。</p>
        <p class="err">当分母是0.9几时，一定不要硬算，结合选项看分子，答案一定比分子大，但是不能大太多，大太多不合理
          （看提问方式那里有没“约”）。</p>
