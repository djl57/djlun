        <p>间隔倍数：= 间隔r + 1</p>
        <p>间隔基期：= 现期/（1 + 间隔r）</p>
        <P>年均增长量：= （现期-基期）/N，其中N为年份差</P>
        <p>年均增长率：（1+r）<sup>n</sup> = 现期/基期，其中n为现期和基期的年份差【年份差一样时，直接比 现期/基期就可以。】【基期：2011年，现期：2015年，年分差：4】
