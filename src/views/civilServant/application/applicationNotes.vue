        <p>比重提问方式：...占...；...中...的占比</p>
        <p>比重公式： = 部分/总体</p>
        <p>引申概念：利润率 = 利润/收入；增长贡献率 = 部分的增长量/总体的增长量</p>
        <p class="err">注意：千万不能分子分母搞反</p>

        <h3 id="ch5-1">现期比重</h3>
        <p>现期比重 = A/B = 部分的现期量/总体的现期量</p>

        <h3 id="ch5-2">基期比重</h3>
        <p>基期比重 = A/B×(1+b)/(1+a) = 部分的现期量/总体的现期量×(1+总体的增长率)/(1+部分的增长率)</p>
        <p>速算：①先截位直除A/B，②再看(1+b)/(1+a)与1的关系（>，&lt;，=），结合选项选答案。</p>

        <h3 id="ch5-3">两期比重</h3>
        <div class="bold">比较：</div>
        <p>遇两期比重比较，看a（部分增速）和b（总体增速）大小：</p>
        <p>（1）a > b，今年比重上升；</p>
        <p>（2）a &lt; b，今年比重下降；</p>
        <p>（3）a = b，今年比重不变。</p>
        <div class="bold">计算：</div>
        <p>提问方式：两个时期 + 比重 + 上升/下降 + 多少个百分点</p>
        <p>现期比重 - 基期比重 = A/B - A/B×(1+b)/(1+a) = A/B×(a-b)/(1+a) = A/B×1/(1+a)×(a-b) &lt; |a-b|</p>
        <p>速算：①判方向【a>b，上升；a&lt;b，下降】；②定大小：&lt;|a-b|</p>
