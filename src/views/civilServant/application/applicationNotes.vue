        <h2 id="ca4">常见词语辨析</h2>
        <p>百分数：表示两个量的比例关系，用除法计算；</p>
        <p>百分点：表示百分数的变化，用加减法计算。由两个百分数作差得来；</p>
        <p>增长量：= 现期/(1+r)×r，速算：①增长率百化分，r=1/n；②增长量=现期/(n+1)，减少量=现期/(n-1)；增长量比较口诀：
          【大大则大（现期大，增长率大，则增长量大），一大一小百化分】</p>
        <p>增长率【增速、增幅、增值率】：增长率 = (现期 - 基期)/基期；</p>
        <p>倍数：是几倍=A/B，多几倍（增长率）= (A - B)/B = A/B - 1 = 倍数 - 1</p>
        <p>增长率和倍数的关系：倍数 = 增长率 + 1</p>
        <p>成数：几成相当于十分之几；</p>
        <p>番数：翻了几番。遇番数，化倍数，翻N番，变为原来的2<sup>N</sup>倍；</p>
        <p>增长率：可正可负，带符号比较；</p>
        <p>降幅：必须为负，不带符号比较；</p>
        <p>变化幅度【变幅】：可正可负，不带符号比较；</p>
