        <p>（3）千位数相加也一样，可以先加千百位数，再加个十位数。</p>

        <h3 id="ch8-1">加减计算</h3>
        <p>（1）精确计算：尾数法，如果末一位相同，则尾两位计算。</p>
        <p>（2）选项是范围：根据选项精度，多截一位计算。</p>
        <p>（3）多个小数相加：先加整数，后加小数。</p>

        <h3 id="ch8-2">排序问题</h3>
        <div class="bold">排序问题四要素：</div>
        <p>（1）时间：现期还是基期</p>
        <p>（2）主体：主体要看清</p>
        <p>（3）单位：单位要一致</p>
        <p>（4）顺序：从大到小？从小到大？</p>
