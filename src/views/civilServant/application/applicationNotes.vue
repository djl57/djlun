        <h2 id="ca3">基期和现期</h2>
        <p>基期：时间靠前【作为对比参照】；现期：时间靠后【相对于基期】</p>
        <p>增长量：+单位；增长率：+%</p>
        <p>同比：与<span class="red">上年</span>同期相比</p>
        <p>环比：与紧紧相邻的上一统计周期相比（月环比，季度环比）</p>
        <p>顺差：出口 - 进口 > 0</p>
        <p>逆差：出口 - 进口 &lt; 0</p>
        <p>同比增长趋势：与上一年一样的增长率</p>

        <h3 id="ch3-1">基期公式</h3>
        <p>（1）基期 = 现期 + 增长量；</p>
        <p>（2）基期 = 现期 / (1 + 增长率)【常考】</p>

        <h3 id="ch3-2">化除为乘</h3>
        <div class="bold">什么时候用？</div>
        <p>求基期时；选项差距小；增长率的绝对值小于等于5%；增长率为负（分母为0.9几）</p>
        <div class="bold">怎么用？</div>
        <p>A/(1-r) ≈ A×(1+r) = A+A×r</p>

        <h3 id="ch3-3">基期和差</h3>
